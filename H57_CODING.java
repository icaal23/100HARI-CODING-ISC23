public class H57_CODING {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            if (i == 3) {
                System.out.println("Loop dihentikan karena i mencapai angka 3");
                break;
            }
            System.out.println("Nilai i: " + i);
        }
    }
}
