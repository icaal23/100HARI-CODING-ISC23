public class H47_CODING {
    public static void main(String[] args) {
        String namaAwal = "Muhammad";
        String namaTerakhir = "Faisal";

        // Menggunakan operator concatenation (+) untuk menggabungkan string
        String namaLengkap = namaAwal + " " + namaTerakhir;
        // Menampilkan hasil concatenation
        System.out.println("Nama Lengkap : " + namaLengkap);
    }
}
