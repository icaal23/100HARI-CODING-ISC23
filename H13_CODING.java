public class H13_CODING {
    public static void main(String[] args) {
        String kata = "Pemrograman Java";
        String huruf_kecil = kata.toLowerCase();
        String huruf_besar = kata.toUpperCase();
        System.out.println("Kata awal = " + kata);
        System.out.println("Method toLowerCase() = " + huruf_kecil);
        System.out.println("Method toUpperCase() = " + huruf_besar);
    }
}
