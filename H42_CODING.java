public class H42_CODING {
    public static void main(String[] args) {
        // Mendefinisikan sebuah string
        String str = "Muhammad Faisal";

        // Menggunakan charAt() untuk mendapatkan karakter pada indeks tertentu
        char character1 = str.charAt(0); // Karakter pertama (indeks 0)
        char character7 = str.charAt(7); // Karakter ketujuh (indeks 7)

        // Menampilkan karakter yang diambil
        System.out.println("Karakter pertama: " + character1);
        System.out.println("Karakter ketujuh: " + character7);
    }
}
